pUsuario usuarioTemp = NULL;

/**
 * @brief Exibe o menu de login e gerencia a autenticação do usuário.
 *
 * Esta função apresenta uma interface de login ao usuário, permitindo que ele insira suas
 * credenciais. Ela autentica o usuário e o direciona para o menu apropriado
 * com base em seu nível de acesso. A função também inclui um caso especial para login de administrador.
 *
 * @note Esta função não recebe parâmetros e não retorna valor.
 *       Ela usa variáveis globais e outras funções para gerenciar o processo de login.
 */
void logarMenu() {
    int idUsuario = 0;
    char senhaTemporaria[31];
    char opcao;

    do {
        printf("=== ReuniON ===\n");
        printf("1. Fazer login\n");
        printf("2. Sair\n");
        printf("Escolha uma opção: ");
        scanf(" %c", &opcao);
        clearInputBuffer();

        if (opcao == '2') {
            printf("Saindo do sistema...\n");
            exit(0);  // Termina o programa
        } else if (opcao == '1') {
            printf("Digite seu id: ");
            scanf("%d", &idUsuario);
            clearInputBuffer();

            printf("Digite sua senha: ");
            scanf("%31[^\n]", senhaTemporaria);
            clearInputBuffer();

            if (idUsuario == 0 && strcmp(senhaTemporaria,"ADMIN") == 0){
                    usuarioTemp = (pUsuario)malloc(sizeof(usuario));
                    usuarioTemp->id = 0;
                    strcpy(usuarioTemp->cpf,"00000000000");
                    strcpy(usuarioTemp->nome,"ADMIN");
                    strcpy(usuarioTemp->perfil,"ADM");
                    strcpy(usuarioTemp->status,"ATIVO");
                    strcpy(usuarioTemp->senha,"ADMIN");
                    exibirMenuADM();
                    break;
            } 

            usuarioTemp = localizarUsuario(idUsuario, senhaTemporaria);

            if (usuarioTemp != NULL) {
                if (strcmp(usuarioTemp->perfil, "COMUM") == 0) {
                    exibirMenuPadrao();
                    break;
                } else if (strcmp(usuarioTemp->perfil, "ADM") == 0) {
                    exibirMenuADM();
                    break;
                }  
                printf("Dados inválidos! Verifique id e senha.\n");
            } else {
                printf("Opção inválida! Tente novamente.\n");
            }
        }
    } while (1);
}



/**
 * @brief Exibe e gerencia o menu padrão do usuário.
 *
 * Esta função apresenta uma interface de menu para usuários padrão, permitindo que eles
 * realizem várias ações como listar salas, fazer reservas e gerenciar suas contas.
 * Ela inicializa os dados de usuários e salas, e então entra em um loop para
 * continuamente exibir opções e processar a entrada do usuário até que ele escolha sair.
 *
 * A função não recebe parâmetros e não retorna valor. Ela depende de variáveis globais
 * e outras funções para gerenciar as interações do usuário e as operações do sistema.
 */
void exibirMenuPadrao() {
    int opcao;
    inicializarUsuario();
    inicializarSalas();

    do {
        printf("=== ReuniON ===\n");
        printf("1. Listar salas\n");
        printf("2. Reservar sala\n");
        printf("3. Listar reservas\n");
        printf("4. Alterar reserva\n");
        printf("5. Cancelar reserva\n");
        printf("6. Alterar senha\n");
        printf("7. Sair\n");

        opcao = obterOpcao();

        switch(opcao){
                case 1:
                    listarSalas();
                    break;
                case 2:
                    reservarSala(usuarioTemp);
                    break;
                case 3:
                    listarReservas(usuarioTemp);
                    break;
                case 4:
                    alterarReserva(usuarioTemp);
                    break;
                case 5:
                    cancelarReserva(usuarioTemp);
                    break;
                case 6:
                    alterarSenha(usuarioTemp);
                    break;
                case 7:
                    printf("Saindo... \n");
                    exit(0);
                    break;
                default:
                    printf("Opção inválida!\n");
        }
        printf("\nPressione ENTER para continuar\n");
        clearInputBuffer();
    } while (opcao != 7);
}



